from pydantic.main import BaseModel

from wow.interface.entity import BlizzardUser


class WAccountBase(BaseModel):
    wow_id: int
    name: str
    realm_id: int
    realm_title: int
    level: int
    faction: str


class WAccountCreate(WAccountBase):
    user_id: int


class WAccount(WAccountBase):
    id: int
    state: int
    user_id: int
    user: BlizzardUser

    class Config:
        orm_mode = True
        arbitrary_types_allowed = True
