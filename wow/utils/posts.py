from database import DatabaseUtils
from wow.blizzard.core import blizzard_db
from wow.database.models import PostCategoryModel


class PostsUtils:

    @staticmethod
    def add_category(user_id, title, url):
        db = blizzard_db()
        return DatabaseUtils.insert(db, PostCategoryModel(
            title=title,
            user_id=user_id,
            url=url,
        ))
